package com.pack1;
public class Student{
	private String name;
	private int roll;
	
	public void setName(String name){
		this.name=name;
	}
	public String getName(){
		return this.name;
	}
	
	public void setRoll(int roll){
		this.roll=roll;
	}
	
	public int getRoll(){
		return this.roll;
	}
}