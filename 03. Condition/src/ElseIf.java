
public class ElseIf {
	//creating method
	public void elseIf() {
		int num=6;
		
		if(num==0) {
			System.out.println("Number is Zero");
		}
		else if(num%2==0) {
			System.out.println("Number is even");
		}
		else if(num%2 !=0) {
			System.out.println("Number is odd");
		}
		else {
			System.out.println("Number is not valid");
		}
		
	}
}
